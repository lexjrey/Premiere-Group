#include <iostream>
#include <cmath>
#include <algorithm>

using namespace std;

int add(int x, int y){
    return x + y;
}

int subtract(int x, int y){
    return x - y;
}

int multiply(int x, int y){
    return x * y;
}

int divide(int x, int y){
    return x/y;
}

int exp(int x, int y){
    return pow(x,y);
}

int mod(int x, int y){
    return x % y;
}
